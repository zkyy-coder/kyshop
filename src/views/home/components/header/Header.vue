      longitude: 116.3972282409668, // 经度，默认天安门
      latitude: 39.90960456049752, // 纬度，默认天安门
      ipAddress: "", // ip地址
    };
  },
  mounted() {
    this.geolocation();
  },
  methods: {
    // 获取定位信息
    geolocation() {
      let geolocation = new qq.maps.Geolocation(
        "SIQBZ-CXFC4-SDHU6-DSPQY-VFFJ3-MFBU3",
        "myapp"
      );
      geolocation.getIpLocation(this.successPosition, this.errorPosition);
    },
    // 获取位置成功的回调
    successPosition(res) {
      // console.log(res);
      this.longitude = res.lng;
      this.latitude = res.lat;
      this.ipAddress = res.province + res.city;
    },
    // 获取位置失败的回调
    errorPosition() {
      this.geolocation();
    },
  },
};
